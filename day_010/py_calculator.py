def sum(a, b): return a + b
def subtraction(a, b): return a - b
def multiply(a, b): return a * b
def division(a, b): return a / b
def power(a, b): return a ** b

operations = {
    '+': sum,
    '-': subtraction,
    '*': multiply,
    '/': division,
    '^': power
}

